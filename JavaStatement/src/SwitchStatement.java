
public class SwitchStatement {
	public static void main(String[] args) {
		
		int a=10;
		
		switch(a) {
		
		case 10:
			System.out.println("select 10");
			break;
		case 20:
			System.out.println("select 20");
			break;
		case 30:
			System.out.println("select 30");
			break;
		default:
		 System.out.println("not 10,20,30");
				
		}
		
		
	}

}
